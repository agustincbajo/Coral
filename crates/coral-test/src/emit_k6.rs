    fn healthcheck_case_for(service: &str) -> TestCase {
        TestCase {
            id: format!("healthcheck:{service}"),
            name: format!("{service} healthcheck"),
            kind: TestKind::Healthcheck,
            service: Some(service.to_string()),
            tags: vec!["healthcheck".into(), "smoke".into()],
            source: TestSource::Inline,
            spec: TestSpec::empty(),
        }
    }

    fn spec_with_http_healthcheck() -> EnvironmentSpec {
        let mut s = spec_with_api_port(3000);
        if let ServiceKind::Real(real) = s.services.get_mut("api").unwrap() {
            real.healthcheck = Some(Healthcheck {
                kind: HealthcheckKind::Http {
                    path: "/healthz".into(),
                    expect_status: 200,
                    headers: BTreeMap::new(),
                },
                timing: HealthcheckTiming::default(),
            });
        }
        s
    }

    #[test]
    fn http_healthcheck_emits_get_and_2xx_check() {
        let case = healthcheck_case_for("api");
        let out = emit_k6(&[case], &spec_with_http_healthcheck());
        assert_eq!(out.included, 1);
        assert!(
            out.script.contains("http.get(`${SVC_API_BASE}/healthz`"),
            "got: {}",
            out.script
        );
        assert!(
            out.script
                .contains("(r) => r.status >= 200 && r.status < 300"),
            "got: {}",
            out.script
        );
    }

    #[test]
    fn healthcheck_probe_label_distinguishes_kinds() {
        let mut s = spec_with_api_port(3000);
        if let ServiceKind::Real(real) = s.services.get_mut("api").unwrap() {
            real.healthcheck = Some(Healthcheck {
                kind: HealthcheckKind::Tcp { port: 3000 },
                timing: HealthcheckTiming::default(),
            });
        }
        assert_eq!(healthcheck_probe_label(&s, "api"), "tcp");
    }

    #[test]
    fn healthcheck_http_path_returns_path() {
        let s = spec_with_http_healthcheck();
        assert_eq!(
            healthcheck_http_path(&s, "api").as_deref(),
            Some("/healthz")
        );
    }
